export default function Cards({ cards, canVote }: CardsProps) {
  const { vote, user, room } = use(RoomContext)

  const myVote = room?.users?.find((u) => u.uid === user?.uid)?.vote

  function handleCardClick(card: string) {
    if (!canVote) return
    try {
      vote(card)
    } catch (error) {
      console.error('Error voting:', error)
    }
  }

  return (
    <Flex align='end'>
      {cards.map((card, index) => (
        <Flex
          onClick={() => handleCardClick(card)}
          zIndex={10 + index}
          key={card}
          position={'relative'}
          p={2}
          cursor='pointer'
          w={{ base: '100px', xl: '120px' }}
          h={myVote === card ? '180px' : { base: '150px', xl: '160px' }}
          bgColor='#FBFBFB'
          borderTopRadius='lg'
          align='center'
          justify='space-between'
          borderColor={myVote === card ? '#F7C379' : '#575757'}
          borderWidth={3}
          borderBottomWidth={myVote === card ? 3 : 0}
          borderBottomLeftRadius={myVote === card ? 'lg' : 0}
          borderBottomRightRadius={myVote === card ? 'lg' : 0}
          ml={index > 0 ? '-20px' : 0}
          direction='column'
          color={myVote === card ? '#F7C379' : '#575757'}
          transition='all 0.3s ease-in-out'
          _hover={
            canVote
              ? {
                  color: '#F7C379',
                  borderColor: '#F7C379',
                  h: '180px',
                  borderBottomWidth: 3,
                  borderBottomLeftRadius: 'lg',
                  borderBottomRightRadius: 'lg',
                  '& > :nth-of-type(2)': {
                    transform: 'translateY(-15px)',
                  },
                  '& > :nth-of-type(3)': {
                    transform: 'translateY(-15px)',
                  },
                  '& > svg': {
                    transform: 'translateY(-15px)',
                  },
                }
              : {}
          }
          css={
            myVote === card
              ? {
                  '& > :nth-of-type(2)': {
                    transform: 'translateY(-15px)',
                  },
                  '& > :nth-of-type(3)': {
                    transform: 'translateY(-15px)',
                  },
                  '& > svg': {
                    transform: 'translateY(-15px)',
                  },
                }
              : {}
          }
        >
          <Text fontSize={14} fontWeight={700} w='full'>
            {card}
          </Text>
          <Text
            fontSize={48}
            textAlign='center'
            fontWeight={800}
            w='full'
            mb='-15px'
            transition='transform 0.3s ease-in-out'
          >
            {card}
          </Text>
          <Text
            fontSize={12}
            w='full'
            fontWeight={700}
            textAlign='end'
            mb='-15px'
            transition='transform 0.3s ease-in-out'
          >
            {card}
          </Text>
          <CardsIcon
            position='absolute'
            w='65%'
            h='95%'
            opacity={0.1}
            transition='transform 0.3s ease-in-out'
          />
        </Flex>
      ))}
    </Flex>
  )
}
